# =============================================================
#  mod1.py  =  "모듈" 파일
#  모듈 = 함수, 변수, 클래스를 모아 놓은 .py 파일.
#  다른 파일에서 import 해서 가져다 쓸 수 있다.
#  (점프 투 파이썬 05-2 모듈 / https://wikidocs.net/29)
# =============================================================

# --- 변수 ---
PI = 3.141592

# --- 함수들 ---
def add(a, b):
    return a + b

def sub(a, b):
    return a - b

# --- 클래스 ---
class Math:
    def solv(self, r):          # 원의 넓이 = PI * r^2
        return PI * (r ** 2)


# =============================================================
#  if __name__ == "__main__":  의 의미
#  - 이 파일을 "직접" 실행하면        -> __name__ 은 "__main__"  => 아래가 실행됨
#  - 다른 파일에서 "import" 하면      -> __name__ 은 "mod1"      => 아래가 실행 안 됨
#  즉, import 할 때는 실행되면 안 되는 "테스트 코드"를 여기에 둔다.
# =============================================================
if __name__ == "__main__":
    print("[mod1 을 직접 실행함]  __name__ =", __name__)
    print("add(1, 4) =", add(1, 4))     # 5
    print("sub(4, 2) =", sub(4, 2))     # 2
