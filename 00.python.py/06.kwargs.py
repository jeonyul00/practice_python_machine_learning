# =========================================================
#  ** (별 2개) = 키워드 인자 => 딕셔너리로 묶인다!
#  이 파일을 실행하면서 출력을 눈으로 따라가 보세요.
# =========================================================


# ---------------------------------------------------------
# [1] **what 은 "이름=값" 들을 딕셔너리로 모은다
# ---------------------------------------------------------
def step1(**what):
    # 여기서 what 은 딕셔너리(dict) 입니다. 직접 찍어봅시다.
    print("what 의 타입 :", type(what))   # <class 'dict'>
    print("what 의 값   :", what)          # {'name': '철수', 'age': 20}

step1(name="철수", age=20)
print("-" * 40)


# ---------------------------------------------------------
# [2] 값을 꺼낼 때는 "문자열 키" 로 꺼낸다  (여기서 아까 에러났음!)
# ---------------------------------------------------------
def step2(**what):
    # what = {'what': 2}  이므로
    # 값 2를 꺼내려면 문자열 키 'what' 을 써야 한다 -> what['what']
    #
    #  ❌ what[what]   : 대괄호 안에 딕셔너리 자체를 넣은 것 => 에러
    #  ✅ what['what'] : 'what' 이라는 키로 값(2)을 꺼냄
    return what['what'] * 2

print("step2 결과 :", step2(what=2))   # 4
print("-" * 40)


# ---------------------------------------------------------
# [3] 여러 개를 넘기면? -> 반복문으로 하나씩 꺼낼 수 있다
# ---------------------------------------------------------
def step3(**info):
    for key, value in info.items():     # .items() = (키, 값) 쌍을 하나씩
        print(f"  {key} => {value}")

step3(이름="영희", 나이=25, 도시="서울")
print("-" * 40)


# ---------------------------------------------------------
# [4] 반대로, 이미 있는 딕셔너리를 ** 로 "풀어서" 넘길 수도 있다
# ---------------------------------------------------------
def step4(name, age):
    print(f"  이름={name}, 나이={age}")

person = {"name": "민수", "age": 30}
step4(**person)   # step4(name="민수", age=30) 과 완전히 똑같음
print("-" * 40)


# ---------------------------------------------------------
# [5] 정리:  *  vs  **
#     *args   -> 값들을 "튜플" 로 묶음      (예: (1, 2, 3))
#     **kwargs-> 이름=값 을 "딕셔너리" 로 묶음 (예: {'a': 1})
# ---------------------------------------------------------
def step5(*args, **kwargs):
    print("  *args   (튜플)      :", args)
    print("  **kwargs(딕셔너리)  :", kwargs)

step5(1, 2, 3, name="철수", age=20)
