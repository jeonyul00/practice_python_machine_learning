import os

# 원문은 "C:/doit/새파일.txt" 처럼 윈도우 경로를 쓰지만,
# 맥에서는 이 스크립트와 같은 폴더에 파일이 생기도록 경로를 잡습니다.
FILE = os.path.join(os.path.dirname(__file__), "새파일.txt")


# =============================================================
# 1. 파일 생성하기  ->  open(경로, 모드)
#    파일 열기 모드 3가지:
#      'r' 읽기 모드  : 내용을 읽기만 함
#      'w' 쓰기 모드  : 새로 씀 (기존 내용이 있으면 싹 지워짐!)
#      'a' 추가 모드  : 기존 내용 뒤에 이어서 씀
#    * 파일을 열었으면 반드시 close() 로 닫아야 함.
# =============================================================
f = open(FILE, 'w')     # 쓰기 모드로 열기 => 빈 파일이 만들어짐
f.close()               # 닫기
print("[1] 파일 생성 완료 :", FILE)
print("=" * 50)


# =============================================================
# 2. 파일에 내용 쓰기  ->  f.write(문자열)
#    \n 은 줄바꿈. 이게 없으면 전부 한 줄로 붙어버림.
# =============================================================
f = open(FILE, 'w')
for i in range(1, 11):                  # 1 ~ 10
    data = "%d번째 줄입니다.\n" % i
    f.write(data)
f.close()
print("[2] 1~10번째 줄을 파일에 씀")
print("=" * 50)


# =============================================================
# 3. 파일 읽는 방법 3가지
# =============================================================

# --- (1) readline() : 한 줄씩 읽기 -------------------------
print("[3-1] readline() 으로 첫 줄만 읽기:")
f = open(FILE, 'r')
line = f.readline()        # 첫 번째 줄
f.close()
print("   ", line.strip())  # strip() 으로 줄 끝 \n 제거

# --- readline() 을 반복해서 전체 읽기 (while) --------------
print("[3-1] readline() + while 로 전체 읽기:")
f = open(FILE, 'r')
while True:
    line = f.readline()
    if not line:           # 더 읽을 줄이 없으면 '' 이 되어 반복 종료
        break
    print("   ", line.strip())
f.close()

# --- (2) readlines() : 모든 줄을 "리스트" 로 읽기 ----------
print("[3-2] readlines() : 모든 줄을 리스트로:")
f = open(FILE, 'r')
lines = f.readlines()      # ['1번째 줄입니다.\n', '2번째 ...', ...]
f.close()
for line in lines:
    print("   ", line.strip())

# --- (3) read() : 파일 전체를 "하나의 문자열" 로 읽기 ------
print("[3-3] read() : 전체를 문자열 하나로:")
f = open(FILE, 'r')
data = f.read()
f.close()
print(data)
print("=" * 50)


# =============================================================
# 4. 파일에 내용 추가하기  ->  'a' (append) 모드
#    'w' 로 열면 기존 내용이 지워지지만, 'a' 는 뒤에 이어붙인다.
# =============================================================
f = open(FILE, 'a')
for i in range(11, 20):                 # 11 ~ 19
    data = "%d번째 줄입니다.\n" % i
    f.write(data)
f.close()
print("[4] 11~19번째 줄을 이어서 추가함")

# 확인
f = open(FILE, 'r')
print("   추가 후 마지막 3줄 :", f.read().strip().split("\n")[-3:])
f.close()
print("=" * 50)


# =============================================================
# 5. with 문과 함께 쓰기  (추천!)
#    - with 블록을 벗어나면 파일이 자동으로 close() 된다.
#    - close() 를 깜빡할 걱정이 없다.
# =============================================================
with open(FILE, 'w') as f:
    f.write("Life is too short, you need python")
# 여기서 자동으로 close 됨

with open(FILE, 'r') as f:
    print("[5] with 로 쓰고 읽기 :", f.read())
print("=" * 50)


