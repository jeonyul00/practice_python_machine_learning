# =============================================================
# 1. import 모듈이름   ->   모듈이름.함수  형태로 사용
#    - import 할 때는 파일명에서 확장자 '.py' 는 빼고 쓴다. (mod1)
# =============================================================
import mod1

print("[1] mod1.add(3, 4) :", mod1.add(3, 4))   # 7
print("[1] mod1.sub(4, 2) :", mod1.sub(4, 2))   # 2
print("=" * 50)


# =============================================================
# 2. from 모듈 import 함수   ->   모듈이름 없이 바로 사용
# =============================================================
from mod1 import add            # add 만 콕 집어서 가져오기
print("[2] add(10, 20) :", add(10, 20))         # 30  (mod1. 안 붙여도 됨)

from mod1 import add, sub       # 여러 개 가져오기 (콤마로)
print("[2] sub(10, 3)  :", sub(10, 3))          # 7

# from mod1 import *            # 모듈 안의 "전부" 가져오기 (* = 전체)
print("=" * 50)


# =============================================================
# 3. 모듈 안의 "변수" 와 "클래스" 도 똑같이 가져다 쓴다
# =============================================================
print("[3] mod1.PI (변수) :", mod1.PI)          # 3.141592

m = mod1.Math()                # 모듈 안의 클래스로 객체 만들기
print("[3] 반지름 2인 원 넓이 :", m.solv(2))     # 12.566368
print("[3] add(PI, 4.4)      :", mod1.add(mod1.PI, 4.4))
print("=" * 50)


# =============================================================
# 4. 핵심 포인트: __name__
#    - 지금 이 파일(11.module.py)을 실행하면 mod1 은 "import" 된 것.
#    - 그래서 mod1.py 아래쪽 if __name__=="__main__": 블록은
#      "실행되지 않는다".  (위 출력에 mod1 의 테스트 문구가 없죠!)
#    - mod1.py 를 직접 실행하면 그때는 그 블록이 실행된다.
# =============================================================
print("[4] 지금 이 파일의 __name__ :", __name__)   # __main__
print("    -> mod1.py 의 __main__ 블록은 위에서 실행 안 됐음을 확인!")
