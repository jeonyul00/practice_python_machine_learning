# =============================================================
#  점프 투 파이썬 "04-4 프로그램의 입출력"  (https://wikidocs.net/180507)
#  터미널(명령 프롬프트)에서 프로그램에 "인수"를 넘겨주는 방법 = sys.argv
#
#  ※ 이 파일은 그냥 실행하지 말고, 뒤에 값을 붙여서 실행하세요!
#     예) python3 09.program_io.py life is too short
# =============================================================
import sys


# =============================================================
#  개념: 명령 프롬프트(터미널) 명령어처럼 인수 주기
#
#  터미널에서 이런 명령을 써본 적 있죠?
#     ls -l              <- ls 뒤의 '-l' 이 ls 에게 준 인수
#     cat 파일.txt        <- cat 뒤의 '파일.txt' 가 cat 에게 준 인수
#
#  내가 만든 파이썬 프로그램도 똑같이 뒤에 값을 붙여서 받을 수 있다.
#     python3 09.program_io.py aaa bbb ccc
#                              └──────┬──────┘
#                              이 값들을 sys.argv 로 받는다
# =============================================================


# =============================================================
# 1. sys.argv = 실행할 때 넘긴 값들의 "리스트"
#    - sys.argv[0]  : 항상 "실행한 파일 이름" 자체
#    - sys.argv[1:] : 내가 뒤에 붙인 진짜 인수들
# =============================================================
print("[1] sys.argv 전체 :", sys.argv)
print("    [0] 파일 이름  :", sys.argv[0])
print("    [1:] 넘긴 인수 :", sys.argv[1:])
print("=" * 50)


# 인수를 하나도 안 넣고 실행한 경우 -> 사용법을 안내하고 종료
args = sys.argv[1:]
if not args:
    print("!! 인수가 없습니다. 이렇게 값을 붙여서 실행해 보세요:")
    print("   python3 09.program_io.py life is too short")
    sys.exit()      # 프로그램 종료 (아래 코드는 실행 안 함)


# =============================================================
# 2. 넘긴 인수들을 하나씩 꺼내 쓰기
# =============================================================
print("[2] 넘긴 인수를 한 줄씩 출력:")
for i in args:
    print("   ", i)
print("=" * 50)


# =============================================================
# 3. 응용 예제 : 넘긴 단어들을 전부 "대문자"로 바꿔 출력
#    (원문 예제와 동일)   .upper() = 대문자 변환
#    print(..., end=' ')  = 줄바꿈 대신 공백으로 이어 붙이기
# =============================================================
print("[3] 전부 대문자로:")
for i in args:
    print(i.upper(), end=' ')
print()   # 마지막에 줄바꿈 한 번
